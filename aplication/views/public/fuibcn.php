                <p>
                  Identidad visual para FIU BCN, un festival para jóvenes creativos emergentes en Barcelona.

                  La campaña de este año se basó en el hashtag #quienfuerajoven ("¡Quién podría ser joven!" En inglés), y se reflejó en el poder de los jóvenes y su fugacidad. Nos inspiramos en La Movida Madrileña, un movimiento clandestino que invadió Madrid y el resto de España durante los años 70 y 80. Tomamos la estética de los 80, las consolas de juegos de 8 bits, los embalajes VHS ... como referencia.

                  El objetivo era crear un universo visual donde la melancolía de un pasado glorioso y la esperanza de un futuro más brillante se combinen entre sí. Para hablar sobre el pasado, dos temas clásicos que aparecen en casi toda la historia del arte. Para representar el futuro, utilizamos una iluminación artificial y psicodélica.

                  Fotografía de Xevi April.
