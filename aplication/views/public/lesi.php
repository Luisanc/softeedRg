                <p>
                  PROBLEMA
                  <br>
                  ¿Alguna vez has intentado rentar una casa? Siempre es un dolor
                   de cabeza hacerlo. Por un lado te piden entregar millones de
                   cosas que ni siquiera sabías que existían, y por otro lado,
                   los lugares que encuentras están muy retirados tu trabajo y
                   amigos. Por si fuera poco, los dueños de las casas sienten
                   mucha desconfianza de rentar por miedo a que su patrimonio
                   pierda valor.
                  <br>
                  SOLUCIÓN
                  <br>
                  Creemos que los avales y los cientos de documentos son cosa del pasado. Estamos desarrollando un sistema de investigación con inteligencia artificial. Aprovechando las redes sociales, historiales crediticios y bancarios podemos llegar a conclusiones bastante fuertes acerca de si una persona es la ótpima o no para rentar una casa y que tanto el inquilino como el propietario queden felices.
                  <br>
                  ¿CÓMO FUNCIONA?
                  <br>
                  Lesi es la alternativa a las plataformas inmobiliarias tradicionales.

                  Durante años la renta de una propiedad se ha basado en la desconfianza hacia la persona que ocupa el inmueble, debido a que el inquilino regularmente es un completo extraño.

                  Hemos visto muchas industrias cambiadas radicalmente en los últimos años; hace no más de una década, una de las premisas principales para los usuarios de internet era nunca conocer en persona a alguien de internet, otra premisa del sentido común era nunca subir al auto de un extraño.

                  Hoy para muchos es un evento común llamar a un extraño de internet para subirnos en su auto y nos lleve hacia nuestro destino.

                  Todo esto gracias a un factor muy simple, pero muy poderoso: nuestra capacidad para medir, censar y recordar es mucho mayor que en cualquier punto en la historia. ¡Y la industria inmobiliaria no puede quedar fuera de esta transformación!

                  ¿Como pretendemos lograr esto?

                  Echaremos mano de uno de los servicios que una persona utiliza mas frecuentemente; sus redes sociales. Analizando el tipo de contenido que publica, podremos formar un juicio de carácter de dicha persona, manteniendo la privacidad de las personas a salvo, ya que todo es ejecutado por nuestro algoritmo de inteligencia artificial.

                  Cientos de modelos de negocios ya utilizan este tipo de algoritmos, un ejemplo muy claro son las empresas de microcréditos y otros servicios basados en big data.

                  Sumado a esto haremos un análisis un poco mas tradicional en entidades como buró de crédito, Bayonet (bayonet.io), historial bancario, calificaciones en servicios ya existentes y otros indicadores, lo cual aunado a la calificación general que el inquilino irá acumulando en nuestra plataforma nos dará una idea muy acertada de quien es y cual es su capacidad de pago.

                  La premisa es sencilla, una vez que hemos validado el perfil de un inquilino como apto, se pondrán a su disposición una serie de inmuebles basados en sus preferencias de ubicación, tipo de inmueble, rango de precio, etc.

                  Aquí es donde entra la segunda pieza clave de nuestro plan de negocios.

                  Muchos de los propietarios de inmuebles interesados en rentar sus propiedades actualmente rentan por su cuenta, lo que les representa no estar respaldados por nadie. Otros han optado por recurrir a inmobiliarias tradicionales, que si bien cumplen con la tarea de acercar clientes a su inmueble, brindan poca o nula protección ante una eventual falta de pago por parte de sus inquilinos e involucran a los propietarios en procesos legales interminables que rara vez son efectivos para efectuar cobranzas.

                  En Lesi proponemos garantizar el pago al propietario en todo momento, lograremos esto de la siguiente manera:

                  El inquilino tendrá mayor motivación a pagar su renta a tiempo gracias a un sistema de descuentos que se activarán si el inquilino mantiene la puntualidad de pago por mas de 6 meses, contemplando 3 escalones de descuento (a falta de un mejor nombre podrían llamarse inquilinos Bronce, Plata y Oro).
                  Buscamos apoyarnos de instituciones como lo son compañías de seguros, que pueden respaldar al inquilino en caso de que éste perdiera su capacidad de pago (seguros de desempleo) y respaldar al propietario en caso de accidentes y situaciones impredecibles en su vivienda (seguros para hogar).
                  La organización de pagos con los inquilinos se adecuará a las fechas en las que el inquilino reciba sus ingresos, siendo posible cobrar por semana, quincena, o mes.

                  Esto nos permitirá crear un sistema mas equilibrado, basado en la confianza entre personas, y escalable a cualquier lugar del mundo.

                  Además, una vez lanzada la plataforma seguiremos integrando servicios que se llevan muy bien con el giro, como es el poder solicitar servicios de limpieza a las casas de los inquilinos desde la misma plataforma y con cobro a la tarjeta registrada.

                  Inicialmente planeamos operar en fase beta en la ciudad de Guadalajara, posteriormente CDMX y Monterrey. Una vez que tengamos el producto listo y probado en las principales ciudades de México, nos expandiremos a toda la república.

                  Hemos estado haciendo investigaciones sobre los requisitos de renta en otros países y encontramos a Colombia, Chile y Argentina como un mercado muy similar, así que queda abierta la posibilidad de expansión a dichos países en el futuro.

                  Sabemos que la manera de rentar casas es tan vieja como la sociedad misma, sin embargo, estamos aquí para cambiar las cosas.
                  <br>
                  TL;DR

                  Una plataforma que, mediante inteligencia artificial ayuda a calificar personas en cuestión de segundos para realizar rentas de inmuebles y así poder evitar muchos trámites burocráticos y complicados. De la misma manera, se protege a los propietarios de los inmuebles mediante seguros de daños a la vivienda, así como domiciliación de los pagos de renta desde la tarjeta de crédito del usuario.
